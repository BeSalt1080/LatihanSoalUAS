public class Pilihan {
    private String teks;
    private boolean benar;
    
    public Pilihan(String teks, boolean benar) {
        this.teks = teks;
        this.benar = benar;
    }

    public String getTeks() {
        return teks;
    }

    public boolean getBenar() {
        return benar;
    }
}
